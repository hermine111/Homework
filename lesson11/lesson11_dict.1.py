# DICTIONARIES
# 1.Write a python function, which add a new value
# with given key in dict.



def add_k_v(dict_ex, new_key, new_value):
    dict_ex[new_key] = new_value
    return dict_ex
print(add_k_v({"avocado":"2", "orange":"8", "kiwi":"10"},
              "apricot", "5"))




























